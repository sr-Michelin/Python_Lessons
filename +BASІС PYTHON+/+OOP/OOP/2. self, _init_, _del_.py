# Метод self
print("\n# ------------self-----------------------")


class Point:
    x, y = 10, 15

    def set_cords(self):
        """self - створює локальні змінні для створеного екземпляра класу;
        обовязковий метод усередині класу
        """
        print(self.__dict__, '-- спосіб 1')

    def set_cords_self(self, x, y):
        """Безпосередній ввід значень у локальні змінні нового екземпляру"""
        self.x, self.y = x, y
        print(self.__dict__, '-- спосіб 2')


# створюємо локальні зміні для екземпляру "pt" і виводим їх у вигляді словника -- спосіб 1
pt = Point()
# -------
pt.x, pt.y = 1, 2
pt.set_cords()

# передаєм x, y безпосередньо у ф-цію із вводу нижче: -- спосіб 2
pt.set_cords_self(2, 4)
# або Point.set_cords_self(pt, 5, 5)

# Конструктор __init__, деструктор __dell__
print("\n# ------------__init__-------------------")


class Point:
    def __init__(self, x=0, y=0):
        """Автоматично викликається при створенні екземпляру класу Point,
        а також передає локальні змінні екземпляру  -- конструктор"""
        print("створення екзепляру класу Point")
        self.x = x
        self.y = y
        print(self.__dict__)

    def __del__(self):
        """Видалення певного екземпляру, якщо у нього відсутні зовнішні послання-- деструктор"""
        print(f'Видалення екземпляру {self.__str__()}')


