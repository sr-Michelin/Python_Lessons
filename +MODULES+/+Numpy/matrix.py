print('–ú–∞—Ç—Ä–∏—Ü—è:\n', a)
print('–í–ª–∞—Å–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è:', w)
print('–í–ª–∞—Å–Ω—ñ –≤–µ–∫—Ç–æ—Ä–∏:\n', v)

